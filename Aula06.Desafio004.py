n = input('Digite algo: ')
print('O tipo é: {}'.format(type(n)))
print('Há {} caracteres.'.format(len(n)))
print('É alphanumérico?', n.isalnum())
print('É alpha?', n.isalpha())
print('É numérico?', n.isnumeric())
print('É um caractere computacional?', n.isascii())
print('É um dígito?', n.isdigit())
print('Está maiúsculo?', n.isupper())
print('Está minúsculo?', n.islower())
print('Só há espaços?', n.isspace())
print('Está capitalizada?', n.istitle())






